// src/services/aiPrompt.js
import { z } from "zod";

/**
 * 💡 생기부 구조 정의
 */
export const recordSchema = z.object({
  autonomous: z.string().describe("자율활동 특기사항 또는 '활동내용없음'")
});

/**
 * 🎓 '꿈꾸는 책방' 종합 분석 프롬프트 빌더
 */
export const buildRecordPrompt = (basePrompt, student, activityText) => {
  return `
    ${basePrompt}

    당신은 대한민국 고등학교의 '꿈꾸는 책방' 지도교사입니다. 
    학생이 작성한 [전체 활동 기록]을 바탕으로 생활기록부 '자율활동'란을 작성하십시오.

    [학생 정보]
    - 학생성명: ${student?.studentName || student?.name || '성명미상'}
    
    [전체 활동 기록 (독서일지 및 자기평가서 통합)]
    ${activityText || "활동 내용 없음"}

    [🌟 작성 시 엄격 준수 사항]
    1. **사실 근거 원칙**: [전체 활동 기록]에 기재된 도서명, 저자, 탐구 주제, 활동 내용을 절대 수정하거나 지어내지 마십시오. 기록에 없는 내용은 창작하지 않습니다.
    2. **도서 정보 우선**: 팀 선정도서보다 학생이 개별 독서일지와 자기평가서에 작성한 도서와 저자 정보를 우선하여 문장을 구성하십시오.
    3. **도서 표기 규칙**: 도서 언급 시 반드시 '도서명(저자)' 형식을 준수하십시오. (예: '어린 왕자(생텍쥐페리)')
    4. **무활동 처리**: 제공된 데이터에 실질적인 활동 내용이 없을 경우, 오직 "활동내용없음"이라고만 출력하십시오.
    5. **분량 및 문체**: 
       - 내용이 풍부하면 300자(900바이트), 부족하면 200자(600바이트) 내외로 조절하십시오.
       - 반드시 '~함', '~임'과 같은 명사형 종결 어미를 사용하십시오.
    6. **비고 제외**: 데이터에 포함된 교사 메모나 비고(remarks) 내용은 생기부 문장에 포함하지 마십시오.

    [🌟 출력 형식]
    - 반드시 순수한 JSON 객체 하나만 반환하십시오: {"autonomous": "내용..."}
  `;
};