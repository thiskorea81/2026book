import { z } from "zod";

/**
 * 💡 생기부 구조 정의 (자율활동 전용)
 * Zod를 통해 AI의 응답이 항상 {"autonomous": "..."} 형식을 갖추도록 강제합니다.
 */
export const recordSchema = z.object({
  autonomous: z.string().describe("자율활동 특기사항 (약 500자, 1500바이트 이내)")
});

/**
 * 🎓 자율활동 초안 생성 프롬프트 빌더
 * 사실에 기반한 기록(Fact-based Recording)을 하도록 가이드를 강화했습니다.
 */
export const buildRecordPrompt = (basePrompt, student, activityText) => {
  return `
    ${basePrompt}

    당신은 대한민국 고등학교의 베테랑 담임교사입니다. 
    학생이 제출한 활동 기록을 바탕으로 생활기록부 '자율활동'란을 작성해야 합니다.

    [학생 정보]
    - 이름: ${student.name || student.studentName}
    - 학번: ${student.userKey || student.studentId}
    
    [핵심 활동 데이터]
    ${activityText}

    [🌟 작성 시 엄격 준수 사항 - 위반 시 무효]
    1. **사실 근거 원칙**: 제공된 [핵심 활동 데이터]에 명시되지 않은 도서명, 저자, 탐구 주제를 절대 허구로 지어내지 마십시오. 읽지 않은 책을 읽었다고 하거나, 하지 않은 실험을 했다고 조작하는 것은 심각한 결격 사유입니다.
    2. **도서 표기 규칙**: 도서가 언급될 경우 반드시 '도서명 (저자)' 형식을 지키십시오. (예: '코스모스 (칼 세이건)')
    3. **NEIS 규격**: 
       - 분량: 한글 기준 약 500자, 공백 포함 1500바이트 이내.
       - 문체: 반드시 '~함', '~임', '~함.'과 같은 명사형 종결 어미를 사용하십시오.
    4. **내용 구성**: 활동의 동기 - 과정 - 결과 - 학생의 변화 및 성장 모습이 논리적으로 연결되도록 전문적인 교육 용어를 사용하여 세련되게 다듬으십시오.

    [🌟 출력 형식 규칙]
    1. 반드시 순수한 JSON 형식으로만 응답하십시오.
    2. 인사말(예: "네, 작성해드리겠습니다")이나 마크다운 기호(\`\`\`json)를 절대 포함하지 마십시오.
    3. 설명이나 사족 없이 오직 아래의 JSON 객체 하나만 출력하십시오.

    예시: {"autonomous": "데이터에 근거한 생기부 문장..."}
  `;
};